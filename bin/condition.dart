
main(){

  // if else statement
  var n =50;
  if(n%2==0){
    print('even');
  }
  else if(n%3==1){
    print('odd');
  }
  else{
    print('confused');
  }

 //switch case

  switch(n){

    case 0:
      print('Even');
      break;

    case 1:
      print('Odd');
      break;

    default:
      print('confused');



  }
}